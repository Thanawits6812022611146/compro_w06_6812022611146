#include <stdio.h>

int main() {
    int a = 5, b = 17;
    float c = 8.5, d = 4.0;

    // บรรทัดที่ 1
    printf("1. แสดงค่าเริ่มต้นของตัวแปรทั้งหมด\n");
    printf("a = %d, b = %d, c = %.2f, d = %.2f\n", a, b, c, d);

    // บรรทัดที่ 2
    printf("2. ผลการคำนวณของ d + a = %.2f\n", d + a);

    // บรรทัดที่ 3
    printf("3. ผลการคำนวณของ a - b = %d\n", a - b);

    // บรรทัดที่ 4
    printf("4. ผลการคำนวณของ c * d = %.2f\n", c * d);

    // บรรทัดที่ 5
    printf("5. ผลการคำนวณของ a * c = %.2f\n", a * c);

    // บรรทัดที่ 6
    printf("6. ผลการคำนวณของ c / d = %.2f\n", c / d);

    // บรรทัดที่ 7
    printf("7. ผลการคำนวณของ b / c = %.2f\n", b / c);

    // บรรทัดที่ 8
    printf("8. ผลการคำนวณของ a %% b = %d\n", a % b);

    // บรรทัดที่ 9
    // หมายเหตุ: ไม่สามารถใช้ % กับ float ได้โดยตรง ต้อง cast เป็น int ก่อน
    printf("9. ผลการคำนวณของ (int)c %% a = %d\n", ((int)c) % a);

    // บรรทัดที่ 10
    printf("10. ผลการคำนวณของ (int)c %% (int)d = %d\n", ((int)c) % ((int)d));

    return 0;
}