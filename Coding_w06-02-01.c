#include <stdio.h>

int main() {
    // บรรทัดที่ 1: กำหนดค่าเริ่มต้นให้ตัวแปร i = 1, j = 2 และยังไม่กำหนดค่า k
    int i = 1, j = 2, k;

    // บรรทัดที่ 2: k = i + j → 1 + 2 = 3
    k = i + j;
    printf("2. k = i + j = %d\n", k);

    // บรรทัดที่ 3: i = i + (k * j) → i = 1 + (3 * 2) = 1 + 6 = 7
    i = i + (k * j);
    printf("3. i = i + (k * j) = %d\n", i);

    // บรรทัดที่ 4: j = i / 2 → j = 7 / 2 = 3 (เพราะเป็น int จะปัดเศษทิ้ง)
    j = i / 2;
    printf("4. j = i / 2 = %d\n", j);

    // บรรทัดที่ 5: k = k / 2 → 3 / 2 = 1
    k = k / 2;
    printf("5. k = k / 2 = %d\n", k);

    // บรรทัดที่ 6: k = (j + k) * 3 → (3 + 1) * 3 = 4 * 3 = 12
    k = (j + k) * 3;
    printf("6. k = (j + k) * 3 = %d\n", k);

    return 0;
}