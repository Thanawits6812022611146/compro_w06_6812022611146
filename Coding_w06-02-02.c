#include <stdio.h>

int main() {
    // บรรทัดที่ 1: กำหนดค่า x = 1.0, y = 2.0 แบบ double (ทศนิยม)
    double x = 1.0, y = 2.0, z;

    // บรรทัดที่ 2: x = y + 5.0 → x = 2.0 + 5.0 = 7.0
    x = y + 5.0;
    printf("2. x = y + 5.0 = %.2f\n", x);

    // บรรทัดที่ 3: y = x / 2.0 → y = 7.0 / 2.0 = 3.5
    y = x / 2.0;
    printf("3. y = x / 2.0 = %.2f\n", y);

    // บรรทัดที่ 4: z = (x + 3.0) * 4.0 → (7.0 + 3.0) * 4.0 = 10.0 * 4.0 = 40.0
    z = (x + 3.0) * 4.0;
    printf("4. z = (x + 3.0) * 4.0 = %.2f\n", z);

    // บรรทัดที่ 5: x = -0.5 * y → -0.5 * 3.5 = -1.75
    x = -0.5 * y;
    printf("5. x = -0.5 * y = %.2f\n", x);

    // บรรทัดที่ 6: z = x + y → -1.75 + 3.5 = 1.75
    z = x + y;
    printf("6. z = x + y = %.2f\n", z);

    return 0;
}