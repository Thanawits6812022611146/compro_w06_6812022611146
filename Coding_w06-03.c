#include <stdio.h>

int main() {
    int x = 12, y = 7, z = 12;

    // 1. x > y → 12 > 7 → จริง (1)
    printf("1. x > y = %d\n", x > y); 

    // 2. x < z → 12 < 12 → เท็จ (0)
    printf("2. x < z = %d\n", x < z); 

    // 3. x == z → 12 == 12 → จริง (1)
    printf("3. x == z = %d\n", x == z); 

    // 4. x != y → 12 != 7 → จริง (1)
    printf("4. x != y = %d\n", x != y); 

    // 5. ((x * 2) <= y) || (5 != (5 / 3))
    //   (12 * 2 = 24 → 24 <= 7 → false) || (5 != 1 → true)
    //   false || true → จริง (1)
    printf("5. ((x * 2) <= y) || (5 != (5 / 3)) = %d\n", ((x * 2) <= y) || (5 != (5 / 3))); 

    // 6. !(x < y) → !(12 < 7) → !false → true → จริง (1)
    printf("6. !(x < y) = %d\n", !(x < y)); 

    // 7. (x + y) > (z * 2) → (12 + 7 = 19) > (12 * 2 = 24) → false → เท็จ (0)
    printf("7. (x + y) > (z * 2) = %d\n", (x + y) > (z * 2)); 

    // 8. (x % 2 == 0) || (y % 2 == 1)
    //     x % 2 == 0 → 12 % 2 == 0 → true
    //     y % 2 == 1 → 7 % 2 == 1 → true
    //     true || true → จริง (1)
    printf("8. (x %% 2 == 0) || (y %% 2 == 1) = %d\n", (x % 2 == 0) || (y % 2 == 1)); 

    // 9. (x > y) && (z < y) → (12 > 7 → true) && (12 < 7 → false)
    //    true && false → เท็จ (0)
    printf("9. (x > y) && (z < y) = %d\n", (x > y) && (z < y)); 

    return 0;
}