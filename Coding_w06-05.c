#include <stdio.h>

int main() {
    int A, B, C, D, E, F, G;

    // A = -2 + 5 * 2;
    // ลำดับการคำนวณ: 5 * 2 = 10 → -2 + 10 = 8
    A = -2 + 5 * 2;
    printf("A = -2 + 5 * 2 → A = %d\n", A); // 8

    // B = 10 / 2 * 3;
    // ลำดับ: 10 / 2 = 5 → 5 * 3 = 15
    B = 10 / 2 * 3;
    printf("B = 10 / 2 * 3 → B = %d\n", B); // 15

    // C = 6 / 2 + 3 * (4 % 2);
    // 6 / 2 = 3, 4 % 2 = 0, 3 * 0 = 0 → 3 + 0 = 3
    C = 6 / 2 + 3 * (4 % 2);
    printf("C = 6 / 2 + 3 * (4 %% 2) → C = %d\n", C); // 3

    // D = (5 + 2) * 15 % 4;
    // (5 + 2) = 7 → 7 * 15 = 105 → 105 % 4 = 1
    D = (5 + 2) * 15 % 4;
    printf("D = (5 + 2) * 15 %% 4 → D = %d\n", D); // 1

    // E = 6 + 2 * 2 - 6 / 2;
    // 2 * 2 = 4, 6 / 2 = 3 → 6 + 4 - 3 = 7
    E = 6 + 2 * 2 - 6 / 2;
    printf("E = 6 + 2 * 2 - 6 / 2 → E = %d\n", E); // 7

    // F = 5 + 3 * 2 - 8 / 4 + (6 % 5);
    // 3 * 2 = 6, 8 / 4 = 2, 6 % 5 = 1 → 5 + 6 - 2 + 1 = 10
    F = 5 + 3 * 2 - 8 / 4 + (6 % 5);
    printf("F = 5 + 3 * 2 - 8 / 4 + (6 %% 5) → F = %d\n", F); // 10

    // G = (4 + 3) * 2 - 10 / (2 + 3);
    // (4 + 3) = 7, (2 + 3) = 5, 7 * 2 = 14, 10 / 5 = 2 → 14 - 2 = 12
    G = (4 + 3) * 2 - 10 / (2 + 3);
    printf("G = (4 + 3) * 2 - 10 / (2 + 3) → G = %d\n", G); // 12

    return 0;
}