#include <stdio.h>

int main() {
    // กำหนดค่าเริ่มต้น
    int a = 5, b = 2;
    float x = 3.0, y = 4.5;

    // นิพจน์ที่ 1: int r1 = a++ * b + (int)y % 3;
    // - a++ ใช้ค่า a ก่อน (5) แล้วค่อยเพิ่มเป็น 6 หลังจากนี้
    // - (int)y คือ 4, 4 % 3 = 1
    // - r1 = 5 * 2 + 1 = 10 + 1 = 11
    int r1 = a++ * b + (int)y % 3;
    printf("r1 = %d\n", r1);

    // นิพจน์ที่ 2: int r2 = (a > b) && ((int)x / b < 2);
    // - หลังจากบรรทัดก่อนหน้า a = 6, b = 2 → (6 > 2) เป็นจริง (1)
    // - (int)x = 3 → 3 / 2 = 1 → 1 < 2 → จริง (1)
    // - r2 = 1 && 1 = 1
    int r2 = (a > b) && ((int)x / b < 2);
    printf("r2 = %d\n", r2);

    // นิพจน์ที่ 3: float r3 = ++x * y - a / 2;
    // - ++x → เพิ่ม x ก่อน → x = 4.0
    // - 4.0 * 4.5 = 18.0
    // - a = 6 → a / 2 = 3
    // - r3 = 18.0 - 3 = 15.0
    float r3 = ++x * y - a / 2;
    printf("r3 = %.2f\n", r3);

    // นิพจน์ที่ 4: float r4 = (x += 1.5) > y || (b-- > 0);
    // - x += 1.5 → x = 5.5
    // - (x > y) = (5.5 > 4.5) → จริง (1)
    // - || short-circuit → ตัวขวาไม่ถูกประเมิน
    // - r4 = 1.0 (แต่เป็น float)
    float r4 = (x += 1.5) > y || (b-- > 0);
    printf("r4 = %.2f\n", r4);

    return 0;
}